'''
    대입연산자
            = ---> 오른쪽의 결과값을 왼쪽에 있는 변수에 저장
    복합대입연산자
            += -= *= /= //= %= **=
            n += 10 ---> n = n + 10
            n -= 10 ---> n = n - 10
            n *= 10 ---> n = n * 10
            n /= 10 ---> n = n / 10
            n //= 10 ---> n = n // 10
            n %= 10 ---> n = n % 10
            n **= 10 ---> n = n ** 10
'''
n = 10
n += 5
print(n)

n = 2
n **= 10
print(n)