for i in range(5): # range(n) : 0 ~ n-1까지 숫자를 하나씩 뽑음, 1씩 증가
    print(i, end=' ')
print('\n--------------------')

for i in range(1,10): # range(s,e) : s~e-1까지 숫자를 하나씩 뽑음, 1씩 증가
    print(i, end=' ')
print('\n--------------------')

for i in range(1,10,2): # range(s,e,a) : s ~ e-1까지 숫자를 a씩 증가해서 뽑음
    print(i,end=' ')
print('\n--------------------')